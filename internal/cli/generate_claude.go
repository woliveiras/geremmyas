package cli

import (
	"os"
	"path/filepath"
	"strings"
)

func generateClaudeCode(root string, artifacts packArtifacts, opts generatorOptions) (generatorSummary, error) {
	content, err := buildIDEAgentsDoc(root, artifacts, "claude-code", "CLAUDE.md")
	if err != nil {
		return generatorSummary{}, err
	}
	summary := generatorSummary{}
	if err := writeGeneratedFile(root, "CLAUDE.md", []byte(content), opts, &summary); err != nil {
		return summary, err
	}
	return summary, nil
}

